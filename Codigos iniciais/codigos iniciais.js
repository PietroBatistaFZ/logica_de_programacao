LOGPRO - Variaveis

//Declarando uma variável e exibindo no console
let nome = ("Pietro")
console.log (nome)

//Declarando um número e exibindo no console
let idade = ("16")
console.log (idade)

//Criando uma variável booleana
let aprendendoJavaScript = ("true")
console.log (aprendendoJavaScript)

//Concatenando strings
let sobrenome = "Batista"
let nomecompleto = (nome) + (sobrenome)
console.log (nomecompleto)

//Somando dois números
let numero1 = 5
let numero2 = 5
let soma = numero1 + numero2
console.log (soma)

//Multiplicando dois números
let numero3 = 7
let numero4 = 7
let multiplicacao = numero3 * numero4
console.log (multiplicacao)

//Variável de texto e número
let cidade = ("Ferraz de Vasconcelos")
let habitantes = ("180.000 habitantes")
console.log (cidade)
console.log (habitantes)

//Alterando o valor de uma variável
let preco = ("100 reais")
preco = ("150 reais")
console.log (preco)

//Criando uma variável sem valor (undefined)
let meuValor
console.log (meuValor)

//Criando um valor null
let meuNulo = ("null")
console.log (meuNulo)

